        public ActionResult Create(multimedia multimedia)
        {
            try
            {
                multimedia.estado = true;
                db.multimedia.AddObject(multimedia);
                db.SaveChanges();
                return RedirectToAction("Index");
            }
            catch (Exception e)
            {
                ErrorModel error = new ErrorModel
                {
                    mensaje = e.InnerException.ToString()
